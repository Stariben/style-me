const LANG_KEY = 'stylematch_lang';

const LangContext = createContext(null);

export function LangProvider({ children }) {
  const [lang, setLangState] = useState(null); // null = not chosen yet

  useEffect(() => {
    const saved = localStorage.getItem(LANG_KEY);
    if (saved && translations[saved]) {
      setLangState(saved);
    }
    // If no saved lang, leave as null → picker will show
  }, []);

  const setLang = (code) => {
    localStorage.setItem(LANG_KEY, code);
    setLangState(code);
  };

  const t = (key) => {
    if (!lang) return translations['fr'][key] || key;
    return translations[lang][key] || translations['fr'][key] || key;
  };

  return (
    <LangContext.Provider value={{ lang, setLang, t, langChosen: !!lang }}>
      {children}
    </LangContext.Provider>
  );
}

export function useLang() {
  return useContext(LangContext);
}